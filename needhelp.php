            <h4 class="d-flex align-items-center"><a class="collapsed" data-bs-toggle="collapse" href="#question2" role="button" aria-expanded="false" aria-controls="question2"><img src="images/arrow-icon.svg" alt="arrow_icon">How do abortion pills work and how are they taken?</a></h4>   
            <p class="collapse" id="question2">Once the decision is made to proceed with a medication abortion, 
                the woman takes a single dose of RU-486. This medication works by blocking the hormone progesterone,
                which is important in maintaining pregnancy. Up to 24 hours later, a dose of misoprostol is taken. 
                This medication works to end the pregnancy by softening the cervix (the opening to the uterus or womb) 
                and causing the uterus to contract. If this process is not effective, 
                a second dose of misoprostol may be taken. Misoprostol can be given under the tongue, in the cheek, or in the vagina.<br>
