        <div class="needhelp-question">
            <h4><a class="collapsed" data-bs-toggle="collapse" href="#question4" role="button" aria-expanded="false" aria-controls="question4"><img src="images/arrow-icon.svg" alt="arrow_icon">Do abortion pills have side effects?</a></h4>   
            <p class="collapse" id="question4">The most common adverse effects associated with abortion pills are gastrointestinal upset 
                (nausea, vomiting, diarrhea, stomach pain), pelvic cramping, and bleeding. Occasionally, dehydration and 
                loss of electrolytes can occur if vomiting and diarrhea are severe. This can be treated with oral or intravenous fluids
                 and nausea medications. Sometimes women have very heavy bleeding with a medication abortion and 
                 may require a blood transfusion or even a surgical procedure. 
                Women with adrenal problems and those on long-term steroids may experience low blood pressure when taking RU-486.  
            </p>
        </div>

